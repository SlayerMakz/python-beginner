# texto o string (str) : "hola" "%&$#@!" "12345"
# integer: 12345, 555, -13, 0
# float: 123.45, -12.34, 0.0
# list: [1, 2, 3], ["hola", "mundo"], [1.2, 3.4, 5.6]
